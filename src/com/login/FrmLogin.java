    private void conUser(){ 
        if(txtUsuario.getText().equals("Ingrese su nombre de usuario")){
            txtUsuario.setText("");
        }
        if(passContra.getText().equals("")){
            passContra.setForeground(new Color(153,153,153));
            passContra.setText("*************");
        } 
    }
    private void conPass(){
        if(passContra.getText().equals("*************")){
            passContra.setText("");
        }
        if(txtUsuario.getText().equals("")){
            txtUsuario.setForeground(new Color(153,153,153));
            txtUsuario.setText("Ingrese su nombre de usuario");
        }
    }
    
    private void enterLogin(java.awt.event.KeyEvent e){
        if(e.getKeyChar() == (char)KeyEvent.VK_ENTER){
                if(txtUsuario.getText().equals("jefferson") || passContra.getText().equals("jeff")){
                    frm.setVisible(true);
                    this.setVisible(false);
                }
                else{
                    //llamamos un frame que indique el error de usuario o contrase√±a
                    message.setVisible(true);
                }
        }
    }
    
